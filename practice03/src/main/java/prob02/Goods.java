package prob02;

public class Goods {
	
	private int name;
	private int price;
	private int count;
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
	
	

	

}
